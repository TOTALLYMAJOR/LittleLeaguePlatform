const envFile = ".env.local";

function parseEnvLine(line) {
  if (!line || line.trim().startsWith("#")) return null;
  const separator = line.indexOf("=");
  if (separator === -1) return null;
  return [line.slice(0, separator).trim(), line.slice(separator + 1).trim()];
}

function loadLocalEnv() {
  if (!existsSync(envFile)) return;
  const lines = readFileSync(envFile, "utf8").split(/\r?\n/);
  for (const line of lines) {
    const entry = parseEnvLine(line);
    if (!entry) continue;
    const [key, value] = entry;
    if (key && !(key in process.env)) process.env[key] = value;
  }
}

function appendMissingEnv(values) {
  const existing = existsSync(envFile) ? readFileSync(envFile, "utf8") : "";
  const additions = Object.entries(values).filter(([key]) => !new RegExp(`^${key}=`, "m").test(existing));
  if (!additions.length) return;

  const body = [
    "",
    "# QA session-bound dashboard users generated by scripts/bootstrap-qa-session-users.mjs",
    ...additions.map(([key, value]) => `${key}=${value}`)
  ].join("\n");
  appendFileSync(envFile, `${body}\n`);

  for (const [key, value] of additions) {
    process.env[key] = value;
  }
}

function generatedPassword() {
  return `Qa-${randomBytes(18).toString("base64url")}-1a`;
}

function requireEnv(name) {
  const value = process.env[name];
  if (!value || value.includes("[YOUR-")) {
    throw new Error(`${name} is required.`);
  }
  return value;
}

async function findUserByEmail(supabase, email) {
  let page = 1;
  const perPage = 200;

  while (page < 20) {
    const { data, error } = await supabase.auth.admin.listUsers({ page, perPage });
    if (error) throw error;
    const found = data.users.find((user) => user.email?.toLowerCase() === email.toLowerCase());
    if (found) return found;
    if (data.users.length < perPage) return null;
    page += 1;
  }

  return null;
}

async function upsertAuthUser(supabase, input) {
  const existing = await findUserByEmail(supabase, input.email);
  if (existing) {
    const { data, error } = await supabase.auth.admin.updateUserById(existing.id, {
      password: input.password,
      email_confirm: true,
      user_metadata: {
        display_name: input.displayName,
        default_role: input.defaultRole
      }
    });
    if (error) throw error;
    return data.user;
  }

  const { data, error } = await supabase.auth.admin.createUser({
    email: input.email,
    password: input.password,
    email_confirm: true,
    user_metadata: {
      display_name: input.displayName,
      default_role: input.defaultRole
    }
  });
  if (error) throw error;
  return data.user;
}

async function upsertOrThrow(supabase, table, row, options = {}) {
  const { data, error } = await supabase
    .from(table)
    .upsert(row, options)
    .select("id")
    .single();
  if (error) throw new Error(`${table} upsert failed: ${error.message}`);
  return data;
}

